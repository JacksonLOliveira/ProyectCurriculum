const campoDataEntrada1 = document.querySelector(".DataEntrada1");
const campoDataSaida1 = document.querySelector(".DataSaida1");
const campoEmpresa1 = document.querySelector(".Empresa1");
const campoCargo1 = document.querySelector(".Cargo1");
const campodescCargo1 = document.querySelector(".descCargo1");
const campoPChaves1 = document.querySelector(".PChaves1");
const send1ExP = document.querySelector(".EnviarExP1");

send1ExP.addEventListener("click", (e) =>{
    e.preventDefault();
    const fullDataEntrada1 = campoDataEntrada1.value;
    const fullDataSaida1 = campoDataSaida1.value;
    const fullEmpresa1 = campoEmpresa1.value;
    const fullCargo1 = campoCargo1.value;
    const fulldescCargo1 = campodescCargo1.value;
    const fullPChaves1 = campoPChaves1.value;
    document.querySelector("#DataEntrada1").innerHTML = fullDataEntrada1;
    document.querySelector("#DataSaida1").innerHTML = fullDataSaida1;
    document.querySelector("#Empresa1").innerHTML = fullEmpresa1;
    document.querySelector("#Cargo1").innerHTML = fullCargo1;
    document.querySelector("#descCargo1").innerHTML = fulldescCargo1;
    document.querySelector("#PChaves1").innerHTML = fullPChaves1;
});

