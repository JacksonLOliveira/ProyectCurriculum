const campoAno1FC = document.querySelector(".ano1FC");
const campoInfos1FC = document.querySelector(".infos1FC");
const send1FC = document.querySelector(".EnviarFC1");

send1FC.addEventListener("click", (e) =>{
    e.preventDefault();
    const fullAno1FC = campoAno1FC.value;
    const fullInfos1FC = campoInfos1FC.value;
    document.querySelector("#anoFC1").innerHTML = fullAno1FC;
    document.querySelector("#infosFC1").innerHTML = fullInfos1FC;
})

